void libh2o_error_printf(const char *format, ...)
{
    char buf[1024];
    va_list args;

    va_start(args, format);
    vsnprintf(buf, sizeof(buf), format, args);
    va_end(args);
    H2O_LOGW("libh2o error: %s", buf);
}
